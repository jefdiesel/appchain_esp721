// Remove duplicates
const chars = [...new Set(SYMBOLS)];

async function sha256(str) {
  return crypto.createHash('sha256').update(str).digest('hex');
}

async function checkName(name) {
  const hash = await sha256(`data:,${name}`);
  try {
    const res = await fetch(`${API}${hash}`);
    const data = await res.json();
    if (!data.result?.exists) {
      console.log(name);
    }
  } catch (e) {
    // retry later
  }
}

async function main() {
  console.error(`Checking ${chars.length} Unicode symbols...`);

  // Process in batches of 50 concurrent requests
  for (let i = 0; i < chars.length; i += 50) {
    const chunk = chars.slice(i, i + 50);
    await Promise.all(chunk.map(checkName));
    console.error(`Progress: ${Math.min(i + 50, chars.length)}/${chars.length}`);
  }
}

main();
